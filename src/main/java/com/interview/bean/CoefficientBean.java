package com.interview.bean;

public class CoefficientBean {

	private String leadingCoefficien;
	private String secondCoefficien;
	private String freeMember;
	private Double discriminant;
	private Double firstVariable;
	private Double secondVariable;

	public String getLeadingCoefficien() {
		return leadingCoefficien;
	}

	public void setLeadingCoefficien(String leadingCoefficien) {
		this.leadingCoefficien = leadingCoefficien;
	}

	public String getSecondCoefficien() {
		return secondCoefficien;
	}

	public void setSecondCoefficien(String secondCoefficien) {
		this.secondCoefficien = secondCoefficien;
	}

	public String getFreeMember() {
		return freeMember;
	}

	public void setFreeMember(String freeMember) {
		this.freeMember = freeMember;
	}

	public Double getDiscriminant() {
		return discriminant;
	}

	public void setDiscriminant(Double discriminant) {
		this.discriminant = discriminant;
	}

	public Double getFirstVariable() {
		return firstVariable;
	}

	public void setFirstVariable(Double firstVariable) {
		this.firstVariable = firstVariable;
	}

	public Double getSecondVariable() {
		return secondVariable;
	}

	public void setSecondVariable(Double secondVariable) {
		this.secondVariable = secondVariable;
	}

}
